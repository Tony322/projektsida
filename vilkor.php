            <h1>Försäljningsvillkor</h1>
			<h3>1. Allmänt</h3>
			<p>
				1.1 Dessa allmänna villkor gäller för speldals produkter, om annat inte särskilt överenskommits skriftligt mellan kunden och speldal.
				<br><br>
				1.2 Vid försäljning till konsument tillämpar speldal de tvingande regler som följer av tillämplig lagstiftning. Mer information om Distans- och hemförsäljningslagen och Konsumentköplagen finns <a href="http://www.riksdagen.se/">här</a>.
				<br><br>
				1.3 Köparen måste vara myndig eller inneha målsmans tillstånd
			</p>
			<h3>2. Beställning</h3>
			<p>
				2.1 Beställning av speldals produkter sker via www.speldal.se. För att en beställning skall vara bindande för speldal, måste beställningen skriftligen bekräftas av speldal före genomförd leverans. Detta innebär bland annat att om produkt i speldal sortiment utgått eller av annan anledning inte finns att tillgå med iakttagande av rimliga ansträngningar, är speldal ej skyldig att tillhandahålla sådan produkt oaktat att kunden beställt densamma enligt ovan.
				<br><br>
				2.2 Vid orderläggning på ett konto registrerat som företag är i alla avseenden det registrerade företaget att ses som avtalsbunden juridisk part.
				<br><br>
				2.3 Vid försäljning av elektronik innehållande tv-mottagare (t.ex. TV,  digitalbox, inspelningsbar DVD, tv-tuner till dator osv.) har vi anmälningsplikt till Radiotjänst i Kiruna AB. Innebörden är att vi inom 30 dagar efter genomförd försäljning måste uppge den information som krävs för att kunna fastställa avgiftsplikten för TV-licens. Det speldal skickar är: namn, adress och telefonnummer på leveransmottagaren. Om vi ej fullgör vår anmälningsplikt kan vi drabbas av böter. Anmälningsplikten styrs av (1989:41) 8 § Lagen om TV-avgift.
			</p>
			<h3>3. Leverans och frakt</h3>
			<p>
				3.1 Leveransdagen är den dag då produkten lämnas ut ifrån speldal lager. Vid leverans fritt kunden är leveransdagen den dag då produkten framkommer till kunden. Avhämtar eller mottager kunden inte produkten på avtalad tid, är leveransdagen den dag då produkten fanns tillgänglig för avhämtning hos speldal. Risken för produkten övergår på kunden vid mottagandet av produkten.
				<br><br>
				3.2 speldal ombesörjer på begäran för kundens räkning transport inklusive transportförsäkring och väljer lämpligast transportsätt efter överenskommelse med kunden. Om speldal ombesörjer transport skall kunden ersätta speldal utgifter i samband med transporten om annat inte överenskommits.
				<br><br>
				3.3 I de fall kunden väljer att inte lösa ut beställd försändelse debiteras kunden kostnad för motsvarande frakt (tur och retur), postförskottsavgift samt hanteringsavgift på 290 kronor. Hanteringsavgiften skiljer sig vid beställning av speldal PC-paket
				<br><br>
				3.4 I de fall där försändelsen av en speldal-tillverkad PC förblir ej uthämtad och returneras till avsändaren eller då kund avbeställer sin PC förbehåller sig speldal Services Sweden AB att ta ut en avgift på 1500:- till 2500:- SEK inkl moms, detta för att täcka produktionkostnader.
				<br><br>
				3.5 Blir din produkt/försändelse skadad i transporten ber vi dig att anmäla händelsen till Kundservice via telefon 031-7102000 eller <a href="mailto:reklamation@speldal.se">reklamation@speldal.se</a>. Dolda transportskador ber vi dig anmäla inom 7 dagar från mottagandet. Synliga transportskador rekommenderar vi dig att anmäla så snart som möjligt från mottagandet. Senast behöver felet anmälas inom rimlig tid som är 2 månader från mottagandet. Föreligger speciella omständigheter kan det anmälas senare. Underlåtelse att följa dessa riktlinjer kan resultera i att kostnaden för produkten inte ersätts av speldal Services Sweden AB.
			</p>
			<h3>4. Leveransförsening</h3>
			<p>	
				4.1 Kunden får genom skriftligt meddelande till speldal häva avtalet såvitt avser försenade produkter om leverans-försening som beror på speldal eller något förhållande på hans sida pågår mer än 45 dagar. speldal åtar sig att återbetala förskottsbetalat belopp inom 20 arbetsdagar.
			</p>
			<h3>5. Pris och betalning</h3>
			<p>	
				5.1 På speldal hemsida och i dess katalog förekommande priser och avgifter är exklusive mervärdeskatt och andra skatter och pålagor samt även exklusive eventuella frakt- eller portokostnader vilka utgår enligt speldal gällande prislista på leveransdagen. speldal.se levererar endast inom Sverige och samtliga produkter säljs med 25% moms.
				<br><br>
				5.2 speldal förbehåller sig rätten att när som helst innan Leveransdagen justera under punkten 5.1 angivna priser, under förutsättning att kunden därvid meddelas. Kunden äger då skriftligen häva köpet i enlighet med punkten 8 nedan.
				<br><br>
				5.3 Vid beställning från speldals hemsida finns möjlighet till betalning med svenska VISA- och Master Card-kort. Efter särskild kreditprövning kan betalning även ske mot faktura. speldal utfärdar faktura på Leveransdagen.
				<br><br>
				5.4 Vi reserverar oss för slutförsäljning, feltryck och prisjusteringar där prissättningen är orimligt låg, i dessa fall förbehåller vi oss rätten att korrigera dessa enligt punkt 5.2.
			</p>	
			<h3>6. Dröjsmål med betalning</h3>
			<p>
				6.1 Betalar inte kunden i rätt tid har speldal rätt att erhålla dröjsmålsränta enligt lag och i förekommande fall hålla på leverans eller del därav.
				<br><br>
				6.2 Är kunden i dröjsmål med betalning mer än 30 dagar efter det speldal anmodat kunden att betala förfallet belopp, får speldal genom skriftligt meddelande till kunden häva avtalet helt eller delvis. Häver speldal avtalet har speldal även rätt till skadestånd.
			</p>
			<h3>7. Ägarförbehåll</h3>
			<p>
				7.1 Produkterna förblir speldal egendom till dess de till fullo betalats.
				<br><br>
				7.2 Till dess äganderätten övergått till kunden, förbinder sig kunden att väl vårda produkterna och att inte utan speldal skriftliga medgivande vidtaga ändringar i dessa. Kunden äger inte rätt att överlåta eller upplåta nyttjanderätt till produkter som omfattas av ägarförbehåll.
			</p>
			<h3>8. Ångerrätt</h3>
			<p>
				8.1 Som kund hos oss har du rätt att ångra ditt köp om du meddelar detta till oss inom 45 dagar från den dagen du mottog produkten. Ångerrätten gäller dock inte datorprogram, operativsystem, spel, ljud- och bildupptagningar om förseglingen/plomberingen brutits. Vid köp av produkter i elektroniskt format t.ex. ljud- eller bildupptagning, spel, och programvarulicenser förlorar du rätten att ångra köpet efter att du mottagit den elektroniska försändelsen eller att licenskod/-information har gjorts tillgänglig. Du är ansvarig endast för varornas minskade värde till följd av annan hantering än vad som är nödvändigt för att fastställa varornas art, egenskaper och funktion. Snabbaste vägen för ångerköp är att följa våra instruktioner på <a href="/k/k.aspx?action=custserv&amp;topic=returns">den här länken</a>. Du kan inte ångra köpet på delar av en vara.
				<br><br><strong>a) </strong>Handlar du som företagare har du 14 dagars ångerrätt och kan endast ångra ditt köp om produktens förpackning är obruten. Om en felrekommendation i kontakt med säljare är gjord gäller 14 dagars ångerrätt även vid bruten förpackning.
				<br><strong>b) </strong>Använda hygienartiklar och kroppsnära produkter så som rakapparater, tandborstar, in-ear hörlurar/hörlurssnäckor m.fl. godkänns inte ångerköp av hygienskäl. Är förpackningen bruten men produkten oanvänd kan du däremot ångra köpet.
				<br><strong>c) </strong>Vid ångerköp på speldal PC (klassas som beställningsvara då de byggs på beställning) tillkommer en avgift på 1500-2500 kr för att täcka en del våra omkostnader.
				<br><strong>d) </strong>Saknas tillhörande originalemballage och/eller tillbehör (kablage, produktmanual etc) vid ångerköp kan speldal göra ett avdrag på 10% av varans värde upp till 500kr.
			</p>
			<h3>9. Garanti</h3>
			<p>
				9.1 Våra produkter levereras med de garantier som tillverkare eller leverantörer erbjuder oss. Innebörden är i första hand reparation om din produkt går sönder. Går produkten inte att reparera byts den mot en likvärdig produkt. Du kan som kund inte åberopa någon ytterligare produktgaranti från speldal. Garantin överlåts till en eventuell andra hands köpare vilket innebär att du som köpt en produkt i andra hand kan få produkten reparerad hos oss. 
				<br><br>
				9.2 Handlar du som privatperson och har du som ursprungskund rätt att reklamera din produkt inom 3 år från den dagen du mottog den. 
				<br><br>
				9.3 Förbrukningsvaror som batterier, bläckpatroner och toners lämnas normalt 6 månaders garanti om inget annat anges på produktsidan.
				<br><br>
				9.4 Näringsidkare har ej rätt att åberopa reklamation efter garantitiden. 
				<br><br>
				9.5 Serienumret måste vara intakt för att garantin skall gälla.
				<br><br>
				9.6 Kom ihåg att skador uppkomna pga för mycket damm i datorn förverkar garantin och reklamationsrätten. Vår rekommendation är att inte låta datorn stå på golvet utan att hänga upp den under skrivbordet. Gör rent den några gånger per år genom luft på burk och öka på så sätt datorns livslängd.
			</p>
			<h3>10. Reklamation</h3>
			<p>
				10.1 Kund som önskar reklamera fel i beställd vara ska inom tre år men alltid så snart som möjligt efter att felet upptäckts kontakta speldal via kundservice på telefon 031-710 20 00 eller via email till <a href="mailto:reklamation@speldal.se">reklamation@speldal.se</a>. 
				<br><br>
				10.2 Om speldal konstaterat att det är fråga om en felaktig vara, och att felet inte beror på någon omständighet på Kundens sida, ska speldal på egen bekostnad avhjälpa felet i varan, eller ersätta varan med en ny vara. speldal står för returfrakten vid giltiga reklamationer. 
			</p>
			<h3>11. Ansvar för fel</h3>
			<p>
				11.1 speldal är skyldig att enligt villkoren nedan och med den skyndsamhet omständigheterna kräver på egen bekostnad avhjälpa fel i produkten som består i att den inte uppfyller avtalad specifikation.
				<br><br>
				11.2 speldal ansvar omfattar inte fel som är utan betydelse för produktens avsedda användning och som inte innebär olägenhet för kunden.
				<br><br>
				11.3 speldal ansvar omfattar inte:
				<br><br><strong>a)</strong> fel förorsakade genom kundens användning av produkterna med annan utrustning eller andra tillbehör på ett sätt som påverkar dess funktion;<br><strong>b)</strong> fel förorsakade genom av kunden företagna ändringar eller ingrepp i produkterna som inte sker i enlighet med speldal instruktioner;<br><strong>c)</strong> fel förorsakade genom kundens användning av produkterna på ett annat sätt än som framgår av användardokumentationen eller genom försummelse av kunden, dennes personal eller tredje man eller genom andra omständigheter utom speldal kontroll;<br><strong>d)</strong> normalt driftsunderhåll såsom justering, normal förslitning eller att anskaffning av förbrukningstillbehör erfordras.
				<br><br>
				11.4 När produkten är svår att flytta kan felavhjälpande ske hos kunden. Vid sådant avhjälpande skall berörda delar ställas till speldal förfogande under därför erforderlig tid. Tid för sådana åtgärder skall förläggas till speldal ordinarie arbetstid. På speldal begäran skall representant från kunden närvara under speldal arbete. I övrigt fall skall felavhjälpandet ske hos speldal eller av denna anvisat serviceställe i Sverige.
				<br><br>
				11.5 Har kunden anmält fel och det visar sig inte föreligga något fel som speldal ansvarar för skall kunden ersätta speldal enligt speldal vid varje tillfälle gällande prislista.
				<br><br>
				11.6 speldal ansvar för fel är begränsat till det ovan sagda såvida inte grov vårdslöshet föreligger. Kunden kan inte rikta andra anspråk mot speldal med anledning av fel.
			</p>
			<h3>12. Ansvarsbegränsning</h3>
			<p>
				12.1 Skulle skadeståndsansvar eller ersättningsskyldighet uppstå för speldal enligt förevarande allmänna villkor skall, såvida inte uppsåt eller grov vårdslöshet föreligger, ansvaret vara begränsat till ett sammanlagt belopp motsvarande köpeskillingen för den eller de produkter varpå skadeståndsskyldigheten grundar sig.
				<br><br>
				12.2 speldal ansvar omfattar endast direkta förluster. speldal ansvar omfattar således inte indirekta förluster, såsom förlust i näringsverksamhet eller kundens förlust av data, och ej heller skador som speldal inte skäligen kunde förväntas ha räknat med vid avtalets träffande och vars följder han inte heller skäligen kunde ha undvikit eller övervunnit.
				<br><br>
				12.3 speldal.se kan inte hållas ansvariga vid förlust av lagrad information eller programvara. Det är kundens skyldighet att ta erforderlig backup på all data innan installation av ny hårdvara, programvara eller annan utrustning sker.
				<br><br>
				12.4 Skulle speldal vara förhindrad att leverera eller företa omleverans alternativt om en sådan leveransplikt blir orimligt betungande som följd av arbetskonflikt eller andra omständigheter som parterna inte kan råda över så som brand, krig, mobilisering eller oväntade militära inkallningar av motsvarande proportioner, rekvisitioner, beslag, valutarestriktioner, uppror och upplopp, transporthinder, varubrist, brist på bränsle, samt förseningar av leveranser från underleverantörer eller producenter som följd av liknande omständigheter som tas upp i denna punkt, är speldal fri från allt ansvar med undantag av reklamationer och kreditering av saknade produkter.

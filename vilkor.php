				<br><br>
				14.2 Om ditt köp avbryts e-postar vi en kom-ihåg-lista till dig över de produkter som du lagt i kundvagnen. Detta är en extra kundservice för att göra ditt köp hos oss så smidigt och enkelt som möjligt. Du kan när som helst återskapa kundvagnen med alla varorna i.<br><br>
				Pris och tillgänglighet på produkterna baseras på tidpunkten då du la produkterna i kundvagnen. Vi förbehåller oss rätten att ändra priserna något i samband med olika kampanjer med utlöpsdatum. Produkterna kan också ha sålts slut.<br><br>
